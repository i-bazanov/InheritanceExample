#include <iostream>
#include <vector>

/////////////////////////////////////////////////////////////////////////
//////////////////////////// class Digit_t //////////////////////////////
/////////////////////////////////////////////////////////////////////////
class Digit_t
{
public:

	virtual const char* getText() const = 0;

	virtual ~Digit_t() {};
};

/////////////////////////////////////////////////////////////////////////
///////////////////////////// class Odin_t //////////////////////////////
/////////////////////////////////////////////////////////////////////////
class Odin_t : public Digit_t
{
public:

	const char* getText() const override
	{
		return "odin";
	}
};

/////////////////////////////////////////////////////////////////////////
///////////////////////////// class Dva_t ///////////////////////////////
/////////////////////////////////////////////////////////////////////////
class Dva_t : public Digit_t
{
public:

	const char* getText() const override
	{
		return "dva";
	}
};

/////////////////////////////////////////////////////////////////////////
///////////////////////////// class Tri_t ///////////////////////////////
/////////////////////////////////////////////////////////////////////////
class Tri_t : public Digit_t
{
public:

	const char* getText() const override
	{
		return "tri";
	}
};

/////////////////////////////////////////////////////////////////////////
//////////////////////////// class Print_t //////////////////////////////
/////////////////////////////////////////////////////////////////////////
class Print_t
{
public:

	Print_t(const std::vector<const Digit_t*>* digits) : m_vecDigits(digits) {};

	void print() const
	{
		for (const Digit_t* digit : *m_vecDigits)
		{
			std::cout << digit->getText() << std::endl;
		}
	}

private:
	const std::vector<const Digit_t*>* m_vecDigits;
};

/////////////////////////////////////////////////////////////////////////
///////////////////////////////// MAIN //////////////////////////////////
/////////////////////////////////////////////////////////////////////////
int main(int argc, char** argv)
{
	Odin_t one;
	Dva_t two;
	Tri_t three;

	std::vector<const Digit_t*> digits{ &one, &two, &three };

	Print_t printer(&digits);

	printer.print();

	return 0;
}
